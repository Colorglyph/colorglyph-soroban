#[test]
fn test_2() {
    let env = Env::default();

    let width: i128 = 22;
    let mut mine_colors = map![&env];
    let mut mint_colors = map![&env];
    
    for i in 0..width {
        for j in 0..width {
            let red = 1.fixed_div_floor(width, 255 - i * 255).unwrap();
            let green = 1.fixed_div_floor(width, 255 - j * 255).unwrap();
            let blue = 1.fixed_div_floor(width * width, i * j * 255).unwrap();

            let color = red * 256i128.pow(2) + green * 256 + blue;

            mine_colors.set(color as u32, 1);
            mint_colors.set(color as u32, vec![&env, (i * j) as u32]);
        }
    }
}

